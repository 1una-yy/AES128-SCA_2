hw_list = np.array([bin(i).count('1') for i in range(256)])


def _mi_histogram(x_vals: np.ndarray, y_vals: np.ndarray, bins: int = 9) -> float:
    """直方圖法估計互資訊 I(X; Y)"""
    N = len(x_vals)
    x_bins = np.arange(bins + 1) - 0.5
    y_bins = np.linspace(y_vals.min(), y_vals.max(), bins + 1)
    joint, _, _ = np.histogram2d(x_vals.astype(float), y_vals, bins=[x_bins, y_bins])
    joint /= N
    px = joint.sum(axis=1)
    py = joint.sum(axis=0)
    mi = 0.0
    for i in range(len(px)):
        for j in range(len(py)):
            if joint[i, j] > 0 and px[i] > 0 and py[j] > 0:
                mi += joint[i, j] * np.log2(joint[i, j] / (px[i] * py[j]))
    return mi


class MIAAttack(BaseAttack):
    name         = "mia"
    display_name = "Mutual Information Analysis (MIA)"
    description  = "用直方圖法估計互資訊，捕捉非線性洩漏特徵進行金鑰恢復。"

    # 每隔幾個採樣點計算（加快速度，改成 1 = 全部計算）
    SAMPLE_STEP = 10

    def run(self, data: AttackInput) -> AttackResult:
        self.validate(data)
        t = (data.preprocessed_traces if data.preprocessed_traces is not None else data.traces).astype(np.float64)
        p = data.plaintexts.astype(np.uint8)
        num_traces, trace_length = t.shape

        sample_points = np.arange(0, trace_length, self.SAMPLE_STEP)
        t_sub = t[:, sample_points]

        mi_max_per_key = np.zeros((16, 256))
        Guess_Key = np.zeros(16, dtype=int)

        for b in range(16):
            for kg in range(256):
                sbox_out = AES_Sbox[p[:, b] ^ kg]
                hw_pred  = hw_list[sbox_out]
                mi_vals  = np.array([
                    _mi_histogram(hw_pred, t_sub[:, s])
                    for s in range(len(sample_points))
                ])
                mi_max_per_key[b, kg] = mi_vals.max()
            Guess_Key[b] = int(np.argmax(mi_max_per_key[b]))

        # 畫圖：每個 byte 各金鑰的最大 MI 長條圖
        fig, axes = plt.subplots(4, 4, figsize=(24, 20))
        for b in range(16):
            ax = axes[b // 4, b % 4]
            ax.bar(range(256), mi_max_per_key[b], color='lightsteelblue', width=1.0)
            ax.bar(Guess_Key[b], mi_max_per_key[b, Guess_Key[b]], color='crimson', width=2)
            ax.set_title(f"Byte {b}  Guess={Guess_Key[b]:02X}h",
                         color='green' if True else 'red', fontsize=10)
            ax.set_xlabel("Key Guess")
            ax.set_ylabel("Max MI")
        plt.tight_layout()

        return AttackResult(
            algorithm    = self.name,
            key_hex      = bytes(Guess_Key.tolist()).hex(),
            key_bytes    = Guess_Key.tolist(),
            num_traces   = num_traces,
            trace_length = trace_length,
            plot_base64  = self.plot_to_base64(fig),
            extra        = {"sample_step": self.SAMPLE_STEP},
        )


registry.register(MIAAttack())
